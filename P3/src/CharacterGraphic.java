/**
 * Interface that Point and Rectangle implement that asks those classes to define the
 * setDrawCharacter method which sets what character will be used in creating that shape
 * @author stephen
 *
 */
public interface CharacterGraphic {
	/**
	 * MEthod to be defined that determines what character will be used in creating the shape
	 * @param c the character to be used
	 */
	public void setDrawCharacter(char c);
}
